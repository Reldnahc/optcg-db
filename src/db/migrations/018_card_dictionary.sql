-- Card dictionary for stable deck hash encoding.
-- Each card_number gets a permanent, append-only index used by the
-- frontend binary deck hash format. Indices must never be reordered.

CREATE TABLE IF NOT EXISTS card_dictionary (
  index     INT PRIMARY KEY,
  card_number TEXT NOT NULL UNIQUE
);

-- Trigger function: auto-assign a dictionary index when a new card is inserted.
CREATE OR REPLACE FUNCTION assign_card_dictionary_index()
RETURNS TRIGGER AS $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM card_dictionary WHERE card_number = NEW.card_number) THEN
    INSERT INTO card_dictionary (index, card_number)
    VALUES (
      COALESCE((SELECT MAX(index) + 1 FROM card_dictionary), 0),
      NEW.card_number
    )
    ON CONFLICT (card_number) DO NOTHING;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_card_dictionary_insert
  AFTER INSERT ON cards
  FOR EACH ROW
  EXECUTE FUNCTION assign_card_dictionary_index();

